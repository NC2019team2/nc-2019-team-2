package com.netcracker.edu.odelivery.model;

import com.netcracker.edu.odelivery.database.annotation.Id;

public class Entity {
    @Id
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
